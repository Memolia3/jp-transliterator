// ローマ字の対応する日本語マップ
export const romajiToKanaMap: TransliterationTable = {};

for (const [kana, romaji] of Object.entries(
  kanaToRomajiMap as TransliterationTable
)) {
  const [hiragana, katakana] = kana.split("|");
  romaji.forEach((roma) => {
    if (!romajiToKanaMap[roma]) {
      romajiToKanaMap[roma] = [hiragana, katakana];
    } else {
      if (!romajiToKanaMap[roma].includes(hiragana)) {
        romajiToKanaMap[roma].push(hiragana);
      }
      if (!romajiToKanaMap[roma].includes(katakana)) {
        romajiToKanaMap[roma].push(katakana);
      }
    }
  });
}
